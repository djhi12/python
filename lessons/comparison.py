"""
Python comparison operators are used to compare values and return a boolean (True or False) result. Here are the most commonly used comparison operators in Python:

== - equal to
!= - not equal to
> - greater than
< - less than
>= - greater than or equal to
<= - less than or equal to

"""

# Here's an example of how you might use these comparison operators in Python:
a = 5
b = 7

print(a == b)  # False
print(a != b)  # True
print(a > b)   # False
print(a < b)   # True
print(a >= b)  # False
print(a <= b)  # True


# In this example, we've assigned the values 5 and 7 to the variables a and b, respectively. We then use the comparison operators to compare the values of these variables and print the results.

