"""
In Python, the lstrip() method is used to remove any leading whitespace characters (spaces, tabs, and newlines) from a string.

"""

# Here's an example:
my_string = "   Hello, World!   "
print(my_string.lstrip())

# Output: "Hello, World!   "

"""
In the example above, "lstrip()" removes the three leading whitespace characters from the original string " Hello, World! ", leaving the remaining string "Hello, World! ".

Note that "lstrip()" does not modify the original string, but instead returns a new string with the leading whitespace removed.

"""