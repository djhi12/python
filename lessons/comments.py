"""
Comments are used in Python to explain code or to make it more readable for other developers. Python comments are lines of code that are not executed by the interpreter. They start with the '#' symbol and continue to the end of the line.

"""

# There are two types of comments in Python:

""" 
Single-line comments: These comments are used to add a short description to a single line of code. They start with the '#' symbol and continue to the end of the line. For example:

"""

# This is a single-line comment
print("Hello, World!")


"""
Multi-line comments: These comments are used to add a longer description to a block of code. They start and end with three consecutive quotes (''' or ""\") and can span multiple lines. 

"""

# For example:
'''
This is a multi-line comment
that spans multiple lines
'''
print("Hello, World!")


"""
It is a good practice to add comments to your code to make it more understandable and maintainable. However, you should not overuse comments, as code that is self-explanatory is often better than code that relies heavily on comments.

"""



