"""
A for loop is a control flow statement that iterates over a sequence of elements, executing a block of code for each element in the sequence. 

"""

# In Python, the syntax for a for loop is as follows:
"""
for variable in sequence:
    # code to be executed

"""

# In this syntax, variable is a variable that is assigned each element of the sequence in turn, and sequence is the sequence of elements that the loop iterates over.


# For example, to print out each element of a list my_list, you could use a for loop like this:
my_list = [1, 2, 3, 4, 5]
for element in my_list:
    print(element)


# This would output:
"""
1
2
3
4
5

"""

"""
You can use any iterable object as a sequence in a for loop, including lists, tuples, dictionaries, sets, and even strings. You can also combine a for loop with conditional statements, functions, and other programming constructs to create more complex programs.

"""