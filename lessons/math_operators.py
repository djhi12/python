"""
Python supports several math operators, including:

Addition: "+"
Subtraction: "-"
Multiplication: "*"
Division: "/"
Modulus (remainder): "%"
Exponentiation: "**"
Floor division (integer division): "//"

"""

# Here are some examples of how to use these operators in Python:
# Addition
x = 10
y = 5
z = x + y
print(z)  # Output: 15

# Subtraction
x = 10
y = 5
z = x - y
print(z)  # Output: 5

# Multiplication
x = 10
y = 5
z = x * y
print(z)  # Output: 50

# Division
x = 10
y = 5
z = x / y
print(z)  # Output: 2.0

# Modulus (remainder)
x = 10
y = 3
z = x % y
print(z)  # Output: 1

# Exponentiation
x = 2
y = 3
z = x ** y
print(z)  # Output: 8

# Floor division (integer division)
x = 10
y = 3
z = x // y
print(z)  # Output: 3


