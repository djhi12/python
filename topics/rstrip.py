"""
    In Python, the rstrip() method is used to remove all trailing whitespace characters from the end of a string. This includes spaces, tabs, and newline characters.
"""

# Here's an example:
string = "Hello world   \n"
print(string.rstrip())

# Output: Hello world

# In the example above, the rstrip() method removes the trailing newline character and the spaces at the end of the string, resulting in the output "Hello world".