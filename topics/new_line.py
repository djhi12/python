"""
    In Python, "\n" represents a newline character. It is used to indicate the end of a line and to start a new line in a string or text file.
"""

# For example, if you wanted to print two sentences on separate lines using the print() function, you could use "\n" to create a line break between them like this:
print("This is the first sentence.\nThis is the second sentence.")

# This would output:
# This is the first sentence.
# This is the second sentence.

