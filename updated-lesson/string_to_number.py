"""
In Python, you can use the input() function to read user input as a string, and then convert it to a number using one of the built-in functions for type conversion.

"""

# Here's an example code snippet that prompts the user to enter a number as a string, converts it to an integer, and then prints the result:
num_str = int(input("Enter a number: "))  # Read input as string
print("The number you entered is:", num_str)


num_str = float(input("Enter a number: "))  # Read input as string
print("The number you entered is:", num_str)
