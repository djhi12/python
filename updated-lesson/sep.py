"""
    In Python, sep is a parameter used in the print() function to specify the separator between the values that are printed.

    By default, the print() function separates values with a space, but you can change this by using the sep parameter. The sep parameter takes a string as its argument and specifies the separator that should be used between the values that are printed.

"""

# Here is an example:
print("Hello", "world", sep="-")

# Output: Hello-world

# In this example, we have used the sep parameter to specify that the separator between "Hello" and "world" should be a hyphen ("-") instead of the default space.  
