# Integer and Float Operations

# Addition
a = 5
b = 2
sum_result = a + b
print(f"Sum: {sum_result}")

# Subtraction
difference = a - b
print(f"Difference: {difference}")

# Multiplication
product = a * b
print(f"Product: {product}")

# Division
quotient = a / b  # Result will be a float
print(f"Quotient: {quotient}")

# Floor Division
floor_quotient = a // b  # Result will be an integer
print(f"Floor Quotient: {floor_quotient}")

# Modulus (Remainder)
remainder = a % b
print(f"Remainder: {remainder}")

# Exponentiation
power_result = a ** b
print(f"Power Result: {power_result}")


