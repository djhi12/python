# Example Program
# Integer example
x = 5
y = 3
sum_of_integers = x + y
print("Sum of integers:", sum_of_integers)

# Float example
pi = 3.14
radius = 2.0
area_of_circle = pi * radius ** 2
print("Area of circle:", area_of_circle)

# String example
first_name = "John"
last_name = "Doe"
full_name = first_name + " " + last_name
print("Full name:", full_name)

# List example
fruits = ["apple", "banana", "cherry"]
fruits.append("orange")
print("Fruits:", fruits)

# Tuple example
point = (3, 4)
x_coord, y_coord = point
print("Coordinates:", x_coord, y_coord)

# Dictionary example
student = {"id": 12345, "name": "Alice", "grade": "A"}
print("Student ID:", student["id"])

# Boolean example
is_raining = False
is_sunny = True
if is_raining:
    print("Take an umbrella.")
elif is_sunny:
    print("Enjoy the sunshine!")

# NoneType example
result = None
if result is None:
    print("No result available.")

# Set example
colors = {"red", "green", "blue"}
colors.add("yellow")
print("Colors:", colors)
